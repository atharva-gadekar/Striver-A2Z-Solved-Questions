// Function to create the list structure
function createListStructure(data, parentElement) {
	const topLevelList = document.createElement("ul");

	// Iterate over the steps
	data.forEach((step) => {
		// Check if the step has sub-steps
		if (step.sub_steps && step.sub_steps.length > 0) {
			const stepItem = document.createElement("li");
			stepItem.textContent = step.title;

			const subStepsList = document.createElement("ul");

			// Iterate over the sub-steps
			step.sub_steps.forEach((subStep) => {
				const subStepItem = document.createElement("li");
				subStepItem.textContent = subStep.title;

				// Check if the sub-step has topics with status 1
				const topicsWithStatus1 = subStep.topics.filter(
					(topic) => topic.status === 1
				);

				if (topicsWithStatus1.length > 0) {
					const topicsList = document.createElement("ul");

					// Iterate over the topics with status 1
					topicsWithStatus1.forEach((topic) => {
						const topicItem = document.createElement("li");
						topicItem.textContent = topic.title;
						topicsList.appendChild(topicItem);
					});

					subStepItem.appendChild(topicsList);
					subStepsList.appendChild(subStepItem);
				}
			});

			if (subStepsList.childElementCount > 0) {
				stepItem.appendChild(subStepsList);
				topLevelList.appendChild(stepItem);
			}
		}
	});

	parentElement.appendChild(topLevelList);
}

createListStructure(updatedJson.data, stepsList);
